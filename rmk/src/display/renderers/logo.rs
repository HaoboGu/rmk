/// Static logo renderer — displays the RMK logo on a 128×32 OLED.
///
/// Draws once and never changes. Useful as a splash screen or idle logo.
#[derive(Default)]
pub struct LogoRenderer;

impl DisplayRenderer<BinaryColor> for LogoRenderer {
    fn render<D: DrawTarget<Color = BinaryColor>>(&mut self, ctx: &RenderContext, display: &mut D) {
        display.clear(BinaryColor::Off).ok();
        if ctx.sleeping {
            return;
        }
        draw_page_format_frame(display, &LOGO, 128, 0, 0);
    }
}

/// Draw a frame stored in SSD1306 page format onto an embedded-graphics display.
fn draw_page_format_frame<D: DrawTarget<Color = BinaryColor>>(
    display: &mut D,
    data: &[u8],
    cols: usize,
    offset_x: i32,
    offset_y: i32,
) {
    let pages = data.len() / cols;
    for page in 0..pages {
        for col in 0..cols {
            let byte = data[page * cols + col];
            if byte == 0 {
                continue;
            }
            for bit in 0..8u32 {
                if byte & (1 << bit) != 0 {
                    let x = col as i32 + offset_x;
                    let y = page as i32 * 8 + bit as i32 + offset_y;
                    Pixel(Point::new(x, y), BinaryColor::On).draw(display).ok();
                }
            }
        }
    }
}
